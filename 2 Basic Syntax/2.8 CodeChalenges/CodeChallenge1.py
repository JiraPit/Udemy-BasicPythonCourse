# ให้ผู้ใช้กรอกชื่อและนามสกุลภาษาอังกฤษ(แยกกัน) 
# แล้ว print ดิกชันนารีที่มี key เป็น ชื่อ และ นามสกุล 
# และมี value เป็นข้อมูลที่ผู้ใช้กรอกโดยเป็นตัวพิมพ์ใหญ่ทั้งหมด

name = input("กรอกชื่อ ")
surname = input("กรอกนามสกุล ")

my_dict = {
    "ชื่อ" : name.upper() ,
    "นามสกุล" : surname.upper()
}

print(my_dict)