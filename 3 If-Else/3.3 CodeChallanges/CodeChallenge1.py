# รับค่าคะแนนจากผู้ใช้ โดย
# ถ้าคะแนนตั้งแต่ 80 ขึ้นไป ให้ print ว่า เกรดA
# ถ้าคะแนนอยู่ในช่วง 70-79 ให้ print ว่า เกรดB
# ถ้าคะแนนอยู่ในช่วง 60-69 ให้ print ว่า เกรดC
# ถ้าคะแนนอยู่ในช่วง 50-59 ให้ print ว่า เกรดD
# ถ้าคะแนนต่ำกว่า 50 ให้ print ว่า เกรดF

score = int(input("กรอกคะแนน: "))

if score >= 80:
    print("เกรดA")
elif score >=70:
    print("เกรดB")
elif score >= 60:
    print("เกรดC")
elif score >= 50:
    print("เกรดD")
else:
    print("เกรดF")    

    